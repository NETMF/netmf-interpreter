namespace System.Xml
{
    /// <summary>
    /// Specifies the current xml:space scope.
    /// </summary>
    public enum XmlSpace
    {
        /// <summary>
        /// No xml:space scope.
        /// </summary>
        None = 0,

        /// <summary>
        /// The xml:space scope is "default".
        /// </summary>
        Default = 1,

        /// <summary>
        /// The xml:space scope is "preserve".
        /// </summary>
        Preserve = 2
    }
}


