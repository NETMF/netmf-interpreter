        static private string GetPathFromURL(string URL)
        {
            char[] szFilePath = URL.ToCharArray();

            for (int i = 0, length = szFilePath.Length; i < length; i++)
            {
                if (szFilePath[i] == '/')
                {
                    szFilePath[i] = '\\';
                }
            }
            // Return updated string with \SD in front
            return new string(szFilePath);
        }




